    static func extractWinnerSet(inputInfo:[DatasetReturnRCInfo]) -> [[DatasetReturnRCInfo]] {
        var resultList:[[DatasetReturnRCInfo]] = []
        var rankResultList: [DatasetReturnRCInfo] = [inputInfo[0]]
        for index in 1..<inputInfo.count {
            if inputInfo[index].rcRank == inputInfo[index - 1].rcRank {
                rankResultList.append(inputInfo[index])
            } else {
                resultList.append(rankResultList)
                rankResultList = [inputInfo[index]]
            }
            if index == inputInfo.count - 1 {
                resultList.append(rankResultList)
            }
        }
        return resultList
    }
    
    
    
