    static func FlyTCsSolution(rcNum: Int, round: Int, everyDealNum: [Int]) -> [[FlyTCsNode]]{
        
        var returnSolutionSet: [[FlyTCsNode]] = []
        var FlyTCResultMatrix : [[FlyTCsNode]] = []
        var FlyTCResultArray: [FlyTCsNode] = []
        
        for i in 0..<round {
            FlyTCResultMatrix.append([])
            for j in 0..<(rcNum * everyDealNum[i]) {
//                第一轮的第一张, 不飞
                if i == 0 && j == 0 {
                    continue
                }
                FlyTCResultMatrix[i].append(FlyTCsNode(X: i, Y: j))
            }
        }
        //整合矩阵成为一个1*MNarray
        for i in 0..<round {
            FlyTCResultArray = FlyTCResultArray + FlyTCResultMatrix[i]
        }
        //最多飞两次
        //第一次
        for i in 0..<FlyTCResultArray.count {
            returnSolutionSet.append([FlyTCResultArray[i]])
        }
        //第二次
        let combinations = FlyTCResultMatrix.combinations(ofCount: 2)
        for combination in combinations {
            let nodesSet = allCombinations(combination)
            returnSolutionSet += nodesSet
        }
        return returnSolutionSet
    }
    
    static func allCombinations(_ arrays: [[FlyTCsNode]]) -> [[FlyTCsNode]] {
        if arrays.isEmpty {
            return [[]]
        }
        
        var result: [[FlyTCsNode]] = []
        let head = arrays[0]
        let tail = Array(arrays.dropFirst())
        let combinationsOfTail = allCombinations(tail)
        
        for element in head {
            for combination in combinationsOfTail {
                result.append([element] + combination)
            }
        }
        
        return result
    }
